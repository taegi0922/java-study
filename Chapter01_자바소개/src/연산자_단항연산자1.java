/*
 * 	1.단항 연산자
 * 	  증감 연산자(++,--) : 1증가,1감소 
 * 		=>반복문에서 주로 사용
 * 		=>게임쪽에서 주로 사용 
 * 		=>전치 연산자 / 후치 연산자  
 * 	
 * 	기능 : 출력 / 증가 
 * 			int a =10;
 * 			System.out.println(a++); // 출력 후 증가 //10
 * 			
 * 			int a =10;
 * 			System.out.println(++a); // 증가 후 출력 //11
 * */
public class 연산자_단항연산자1 {
	public static void main(String[] args) {
		int a =10;
		System.out.println(a++);
		
	}

}
