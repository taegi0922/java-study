/*
 * 	자바에서 제공하는 반복문 / 제어문 
 * 		=> 목록 출력
 * 		=> 반복문의 종류
 * 			1) for => 반복문중에서 가장 많이 사용되는 제어문 
 * 			2) while => 데이터베이스 / 파일 읽기 
 * 			3) do~while => 자바에서는 사용 빈도가 적다.
 * 			4) 쉬프트연산자 / 비트연산자 / 반전연사자 => 웹에서는 사용하지 않는다.
 * 
 * */
public class 반복문_1 {

	public static void main(String[] args) {
		// A~Z 
		for(char i =65; i<=90; i++) {
			System.out.print(i);
		}
	}

}
